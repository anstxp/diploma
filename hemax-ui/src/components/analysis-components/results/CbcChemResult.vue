<template>
  <div class="result-card">
    <ResultHeader
      :type="analysis.type"
      :created-at="analysis.createdAt"
      :severity="severity"
    />

    <div v-if="topFlag" class="top-flag">
      <i class="fa-solid fa-flag"></i>
      <span>{{ topFlag }}</span>
    </div>

    <ToneBanner :tone="tone" :summary="summary" />

    <div v-if="summary && (!tone || tone === 'normal')" class="summary">
      <h4>{{ $t('results.headers.summary') }}</h4>
      <p>{{ summary }}</p>
    </div>

    <StoryCards :stories="stories" />

    <LabValuesList :kind="kind" :raw-values="rawValues" />

    <DoctorNote :note="doctorNote" />

    <RawJsonDisplay
      :raw-input-json="analysis.rawInputJson"
      :result-json="analysis.resultJson"
    />
  </div>
</template>

<script>
import ResultHeader   from "./shared/ResultHeader.vue";
import ToneBanner     from "./shared/ToneBanner.vue";
import StoryCards     from "./shared/StoryCards.vue";
import LabValuesList  from "./shared/LabValuesList.vue";
import DoctorNote     from "./shared/DoctorNote.vue";
import RawJsonDisplay from "./shared/RawJsonDisplay.vue";
import { normalizeKindCode } from "@/constants/analysisConstants";

/**
 * Renders CBC and CHEM analyses. Both share the same shape:
 *   - tone-driven narrative (ToneBanner + StoryCards)
 *   - lab values table with reference-range flagging
 *   - optional doctor note
 *
 * The narrative text (summary, stories) was generated by the analyzer in
 * the language the user requested at submit time — so this component itself
 * only needs to localize fixed UI labels (headings, badges).
 */
export default {
  name: "CbcChemResult",
  components: {
    ResultHeader, ToneBanner, StoryCards, LabValuesList, DoctorNote, RawJsonDisplay,
  },
  props: {
    analysis: { type: Object, required: true },
  },
  computed: {
    kind()      { return normalizeKindCode(this.analysis.type); },
    severity()  {
      return this.analysis.severityLabel ?? this.analysis.severityName
          ?? this.analysis.severity ?? this.analysis.result?.severity ?? "Unknown";
    },
    result()    { return this.analysis.result || {}; },
    topFlag()   { return this.analysis.topFlag || this.result.topFlag || null; },
    summary()   {
      return this.analysis.summaryUa || this.analysis.summary || this.result.summary || null;
    },
    tone()      { return this.result.tone || this.result.narrative?.tone || null; },
    stories()   { return this.result.stories || this.result.narrative?.stories || []; },
    rawValues() { return this.result.values || this.result.normalised || {}; },
    doctorNote(){ return this.analysis.doctorNote || null; },
  },
};
</script>
